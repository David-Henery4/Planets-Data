

const colourSelection = (name) => {
  if (name === "Mercury") return "bg-skyBlue";
  if (name === "Venus") return "bg-goldish";
  if (name === "Earth") return "bg-purple";
  if (name === "Mars") return "bg-lightOrange";
  if (name === "Jupiter") return "bg-red";
  if (name === "Saturn") return "bg-darkOrange";
  if (name === "Uranus") return "bg-mintGreen";
  if (name === "Neptune") return "bg-brightBlue";
}

export default colourSelection